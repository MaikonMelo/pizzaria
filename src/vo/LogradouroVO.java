public class LogradouroVO {

    private String log_cep;
    private int log_estado;
    private String log_cidade;
    private String log_logradouro;
    private String log_bairro;

    public String getLog_cep() {
        return log_cep;
    }

    public void setLog_cep(String log_cep) {
        this.log_cep = log_cep;
    }

    public int getLog_estado() {
        return log_estado;
    }

    public void setLog_estado(int log_estado) {
        this.log_estado = log_estado;
    }

    public String getLog_cidade() {
        return log_cidade;
    }

    public void setLog_cidade(String log_cidade) {
        this.log_cidade = log_cidade;
    }

    public String getLog_logradouro() {
        return log_logradouro;
    }

    public void setLog_logradouro(String log_logradouro) {
        this.log_logradouro = log_logradouro;
    }

    public String getLog_bairro() {
        return log_bairro;
    }

    public void setLog_bairro(String log_bairro) {
        this.log_bairro = log_bairro;
    }
}
