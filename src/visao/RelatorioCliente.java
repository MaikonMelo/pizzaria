    private JFileChooser criaChooser(){
        JFileChooser jFileChooser=new JFileChooser("%HomePath");
        FileNameExtensionFilter filtro=new FileNameExtensionFilter("Planilha do Excel *.xls", "xls");
        FileNameExtensionFilter filtro2=new FileNameExtensionFilter("Documento do Word *.doc", "doc");
        jFileChooser.setAcceptAllFileFilterUsed(false);
        jFileChooser.addChoosableFileFilter(filtro);
        jFileChooser.addChoosableFileFilter(filtro2);
        return jFileChooser;
    }
