    private void btGerarRelatorioClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGerarRelatorioClienteActionPerformed
        RelatorioClienteRN rel=new RelatorioClienteRN();
        JFileChooser jFileChooser = criaChooser();
        int retorno=jFileChooser.showSaveDialog(null);
        if(retorno==JFileChooser.APPROVE_OPTION){
            try{
                String arquivo=jFileChooser.getFileFilter().getDescription();
                String ext="."+arquivo.substring(arquivo.lastIndexOf(".")+1);
                String caminho=(jFileChooser.getSelectedFile().toString()+ext);
                File file =null;
                file=rel.gerarRelatorio(Long.parseLong(txtCodigo.getText()), caminho);
                if(file!=null){
                    JOptionPane.showMessageDialog(null, "Relatório gerado com sucesso", "Relatorio", JOptionPane.INFORMATION_MESSAGE);
                    Desktop.getDesktop().open(file);
                }
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, "Erro ao gerar Relatório."+ex, "Relatório", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btGerarRelatorioClienteActionPerformed

