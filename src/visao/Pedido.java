    private void btAddPizzaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddPizzaActionPerformed

        if(!"".equals(txtTelefone.getText())){
            PedidoVO pedidoGerado = new PedidoVO();
            
            if("Selecione...".equals(cbSabor1.getSelectedItem().toString()) && "Selecione...".equals(cbSabor2.getSelectedItem().toString()) && "Selecione...".equals(cbSabor3.getSelectedItem().toString())){
                JOptionPane.showMessageDialog(null,"A pizza deve conter ao menos 1 sabor !","Pedido",JOptionPane.INFORMATION_MESSAGE);

            }else if("Selecione...".equals(cbTamanho.getSelectedItem().toString())){
                JOptionPane.showMessageDialog(null,"A pizza deve conter um tamanho !","Pedido",JOptionPane.INFORMATION_MESSAGE);

            }else{
                try {
                    if("Selecione...".equals(cbSabor1.getSelectedItem().toString()))
                    pedidoGerado.setSabor1("X");
                else
                    pedidoGerado.setSabor1(cbSabor1.getSelectedItem().toString());

                if("Selecione...".equals(cbSabor2.getSelectedItem().toString()))
                    pedidoGerado.setSabor2("X");
                else
                    pedidoGerado.setSabor2(cbSabor2.getSelectedItem().toString());

                if("Selecione...".equals(cbSabor3.getSelectedItem().toString()))
                    pedidoGerado.setSabor3("X");
                else
                    pedidoGerado.setSabor3(cbSabor3.getSelectedItem().toString());

                if("Selecione...".equals(cbBorda.getSelectedItem().toString()))
                    pedidoGerado.setBorda("X");
                else
                    pedidoGerado.setBorda(cbBorda.getSelectedItem().toString());


                pedidoGerado.setQuant(Integer.parseInt(spQuant.getValue().toString()));
                pedidoGerado.setTamanho(cbTamanho.getSelectedItem().toString());

                PedidoRN pedidorn = new PedidoRN();
                PedidoVO retorno = pedidorn.valorPizza(pedidoGerado);
                pedidoGerado.setValor(retorno.getValor());

                totalPedido = totalPedido + retorno.getTotal();
                String totalTela = totalPedido.toString();
                txtTotal.setText("R$ "+totalTela.format("%.2f", totalPedido));

                listaPedidoPizza.add(pedidoGerado);

                DefaultTableModel modelo = (DefaultTableModel)tbPizza.getModel();
                modelo.setRowCount(0);
                for (PedidoVO listarTabela : listaPedidoPizza) {
                    modelo.addRow(new Object[]{listarTabela.getTamanho(), listarTabela.getSabor1(), listarTabela.getSabor2(), listarTabela.getSabor3(), listarTabela.getBorda(), listarTabela.getQuant(), listarTabela.getValor()});
                }
                tbPizza.setModel(modelo);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null,"Erro ao inserir Pizza! ","Pedido",JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }else
            JOptionPane.showMessageDialog(null,"Primeiramente selecione um cliente! ","Pedido",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btAddPizzaActionPerformed

