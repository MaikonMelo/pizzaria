    private void popularCombo() {
        try {
            PedidoRN pedidorn = new PedidoRN();
            
            cbSabor1.removeAllItems();
            cbSabor2.removeAllItems();
            cbSabor3.removeAllItems();
            cbBorda.removeAllItems();
            cbBebida.removeAllItems();
            cbUF.removeAllItems();
            
            cbSabor1.addItem("Selecione...");
            cbSabor2.addItem("Selecione...");
            cbSabor3.addItem("Selecione...");
            cbBorda.addItem("Selecione...");
            cbBebida.addItem("Selecione...");
            cbUF.addItem("Selecione...");
            
            ArrayList<ConsultarClienteVO> comboEstado = pedidorn.listarComboEstado();
            for (ConsultarClienteVO clientevo : comboEstado) {
                cbUF.addItem(clientevo.getEstado());
            }
            
            ArrayList<PedidoVO> comboSabor = pedidorn.listarComboSabor();
            for (PedidoVO pedidovo : comboSabor) {
                cbSabor1.addItem(pedidovo.getSabor());
                cbSabor2.addItem(pedidovo.getSabor());
                cbSabor3.addItem(pedidovo.getSabor());
            }
            ArrayList<PedidoVO> comboBorda = pedidorn.listarComboBorda();
            for (PedidoVO pedidovo : comboBorda) {
                cbBorda.addItem(pedidovo.getBorda());
            } 
            
            ArrayList<PedidoVO> comboBebida = pedidorn.listarComboBebida();
            for (PedidoVO pedidovo : comboBebida) {
                cbBebida.addItem(pedidovo.getBebida());
            } 
            
            
            
        } catch (SQLException | ClassNotFoundException ex) {
            
        } catch (Exception ex) {
            Logger.getLogger(Pedido.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
