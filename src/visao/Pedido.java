    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        int n = JOptionPane.showConfirmDialog(null,"Deseja alterar dados do Cliente? ","Cliente",JOptionPane.YES_NO_OPTION);
        if(n==0){
            ConsultarClienteVO cliente = new ConsultarClienteVO();
            
            cliente.setTelefone(Long.parseLong(txtTelefone.getText()));
            cliente.setNome(txtNome.getText());
            cliente.setCep(txtCEP.getText());
            cliente.setCidade(txtCidade.getText());
            cliente.setBairro(txtBairro.getText());
            cliente.setLogradouro(txtLogradouro.getText());
            cliente.setNumerocasa(Integer.parseInt(txtNumero.getText()));
            cliente.setComplemento(txtComplemento.getText());
            cliente.setEstado(cbUF.getSelectedItem().toString());
            
            PedidoRN pedidorn = new PedidoRN();
            try {
                pedidorn.alterarCliente(cliente);
            } catch (Exception ex) {
                Logger.getLogger(Pedido.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null,"Cliente alterado com sucesso! ","Cliente",JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

