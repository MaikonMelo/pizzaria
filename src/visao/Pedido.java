    private void btRemPizzaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemPizzaActionPerformed
        
        int selecionada = tbPizza.getSelectedRow();
        
        try {

            DefaultTableModel modelo = (DefaultTableModel)tbPizza.getModel();
            modelo.setRowCount(0);
            for (PedidoVO listarTabela : listaPedidoPizza) {
                modelo.addRow(new Object[]{listarTabela.getTamanho(), listarTabela.getSabor1(), listarTabela.getSabor2(), listarTabela.getSabor3(), listarTabela.getBorda(), listarTabela.getQuant(),listarTabela.getValor()});
            }
            listaPedidoPizza.remove(selecionada);
           
            String valorTemporario = modelo.getValueAt(selecionada, 6).toString();
            totalPedido = totalPedido - Float.parseFloat(valorTemporario.replace(" ", "").replace("R", "").replace("$", "").replace(",", "."));
            modelo.removeRow(selecionada);
            String tratandoTotal = totalPedido.toString();
            txtTotal.setText("R$ "+tratandoTotal.format("%.2f", totalPedido));
            
            
            tbPizza.setModel(modelo); 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Uma pizza deve ser selecionada!","Pedido",JOptionPane.INFORMATION_MESSAGE);
        }
        btRemPizza.setEnabled(false);
        
    }//GEN-LAST:event_btRemPizzaActionPerformed

