    @Override
    public void btEditarActionPerformed(ActionEvent e) {
        txtPreco.setEnabled(true);
        txtVolume.setEnabled(true);
        cbMarca.setEnabled(true);
        cbTipo.setEnabled(true);
        cbVolume.setEnabled(true);
        btTipo.setEnabled(true);
        btMarca.setEnabled(true);
       
        
        int selecionada = tbTemplate.getSelectedRow();
        String vtemp = ((String) tbTemplate.getValueAt(selecionada,4));
            txtCodigo.setText((tbTemplate.getValueAt(selecionada, 0).toString()));
            cbMarca.setSelectedItem(tbTemplate.getValueAt(selecionada,2));
            cbTipo.setSelectedItem(tbTemplate.getValueAt(selecionada,3));
            txtPreco.setText(vtemp.replace(" ", "").replace("R","").replace("$","").replace(",","."));
            cbVolume.setSelectedItem(tbTemplate.getValueAt(selecionada,5));
            txtVolume.setText((tbTemplate.getValueAt(selecionada, 6).toString()));  
    }

    @Override
    public void btExcluirActionPerformed(ActionEvent e) {
        txtPreco.setEnabled(false);
        txtVolume.setEnabled(false);
        cbMarca.setEnabled(false);
        cbTipo.setEnabled(false);
        cbVolume.setEnabled(false);
        btTipo.setEnabled(false);
        btMarca.setEnabled(false);
        int n = JOptionPane.showConfirmDialog(null,"Deseja excluir a bebida? ","Bebida",JOptionPane.YES_NO_OPTION);
        if(n==0){
            
            try{
            BebidaVO bebidaVO = new BebidaVO();
     
            int selecionada = tbTemplate.getSelectedRow();
            bebidaVO.setCodigo((int) tbTemplate.getValueAt(selecionada, 0));
            
            BebidaRN bebidaRN = new BebidaRN();
            bebidaRN.excluirBebida(bebidaVO);
           
            
            atualizarLista();
            
            //buscaBorda.popularTabela();
            
        } catch (SQLException w) {

            JOptionPane.showMessageDialog(null, "Erro: " + w.getMessage(), "Exclusão de Borda", JOptionPane.ERROR_MESSAGE);
        
        } catch (Exception w) {

            JOptionPane.showMessageDialog(null, "Erro: " + w.getMessage(), "Exclusão de Borda", JOptionPane.ERROR_MESSAGE);
        }
          JOptionPane.showMessageDialog(null,"Bebida excluida com sucesso! ","Bebida",JOptionPane.INFORMATION_MESSAGE);
    
        
        }
    }
