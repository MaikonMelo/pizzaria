    public final void atualizarLista(){
        try{
            BebidaRN bebidarn = new BebidaRN();
            ArrayList<BebidaVO> bebidas = bebidarn.listarBebida();
            
            DefaultTableModel modelo = (DefaultTableModel)tbTemplate.getModel();
            modelo.setRowCount(0);
           
            
            for (BebidaVO bebidaVO : bebidas) {
                
                modelo.addRow(new Object[]{bebidaVO.getCodigo(),bebidaVO.getNome(),bebidaVO.getMarca(),bebidaVO.getTipo(), bebidaVO.getPreco(), bebidaVO.getUnidade(), bebidaVO.getVolume()});
            }
            tbTemplate.setModel(modelo);
            
            ajustarTabelaBorda();
            
            
    
        }catch (SQLException | ClassNotFoundException ex) {
           
        } catch (Exception ex) {
            Logger.getLogger(BebidaRN.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    public final void listarBebida(){
    
        try {
            BebidaRN bebidarn = new BebidaRN();
            ArrayList<BebidaVO> bebida = bebidarn.listarBebida();
            
            DefaultTableModel modelo = (DefaultTableModel)tbTemplate.getModel();
            modelo.setRowCount(0);
           
            int n;
          
            for(n = 0; n<=6 ;n++)
                modelo.addColumn(this);
            
           
            
            for(BebidaVO bebidaVO : bebida) {
                
                modelo.addRow(new Object[]{bebidaVO.getCodigo(),bebidaVO.getNome(),bebidaVO.getMarca(),bebidaVO.getTipo(), bebidaVO.getPreco(), bebidaVO.getUnidade(), bebidaVO.getVolume()});
            }
            tbTemplate.setModel(modelo);
            
            ajustarTabelaBorda();
            
            
        } catch (SQLException | ClassNotFoundException ex) {
           
        } catch (Exception ex) {
            Logger.getLogger(BebidaRN.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
