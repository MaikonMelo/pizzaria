    public void popularTabelaPrincipal() {

        try {

            PedidoRN pedidoRN = new PedidoRN();

            pedido.clear();
            pedido = pedidoRN.buscarPedido();

            DefaultTableModel modelo = (DefaultTableModel) tbPedido.getModel();

            modelo.getDataVector().clear();
            modelo.setRowCount(0);

            for (ConsultarPedidoVO pedidoVO : pedido) {

                modelo.addRow(new Object[]{
                    pedidoVO.getPedido_codigo(),
                    pedidoVO.getCliente_telefone(),
                    pedidoVO.getCliente_nome()
                });
            }
            tbPedido.setModel(modelo);

        } catch (SQLException sqle) {

            JOptionPane.showMessageDialog(null, "Erro: " + sqle.getMessage(), "Pedido", JOptionPane.ERROR_MESSAGE);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Erro: " + e.getMessage(), "Pedido", JOptionPane.ERROR_MESSAGE);
        }
    }

