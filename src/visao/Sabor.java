    @Override
    public void btSalvarActionPerformed(ActionEvent e) {
        txtIngredientes.setEnabled(false);
        txtPreco.setEnabled(false);
        txtSabor.setEnabled(false);
        rbDoce.setEnabled(false);
        rbSalgada.setEnabled(false);
        rbAtivo.setEnabled(false);
        rbInativo.setEnabled(false);

        if (save == 1) {

            try {

                ConsultarSaborVO saborVO = new ConsultarSaborVO();

                saborVO.setSab_nome(txtSabor.getText());
                saborVO.setSab_preco(Float.parseFloat(txtPreco.getText()));
                saborVO.setSab_tipo(rbDoce.isSelected() ? "Doce" : "Salgado");
                saborVO.setSab_status(rbAtivo.isSelected());
                saborVO.setSab_ingredientes(txtIngredientes.getText());

                SaborRN saborRN = new SaborRN();
                saborRN.cadastrarSabor(saborVO);

                popularTabelaSabor();

                JOptionPane.showMessageDialog(null, "Sabor Cadastrado com Sucesso.", "Cadastro de Sabor", JOptionPane.INFORMATION_MESSAGE);

                txtCodigo.setText("");
                txtIngredientes.setText("");
                txtPreco.setText("");
                txtSabor.setText("");

            } catch (SQLException ex) {

                JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage(), "Cadastro de Sabor", JOptionPane.ERROR_MESSAGE);

            } catch (Exception ex) {

                JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage(), "Cadastro de Sabor", JOptionPane.ERROR_MESSAGE);
            }

        } else if (save == 2) {

            try {

                ConsultarSaborVO saborVO = new ConsultarSaborVO();

                saborVO.setSab_codigo(Integer.parseInt((String) tbTemplate.getValueAt(tbTemplate.getSelectedRow(), 0)));
                saborVO.setSab_nome(txtSabor.getText());
                saborVO.setSab_preco(Float.parseFloat(txtPreco.getText()));
                saborVO.setSab_tipo(rbDoce.isSelected() ? "Doce" : "Salgado");
                saborVO.setSab_status(rbAtivo.isSelected());
                saborVO.setSab_ingredientes(txtIngredientes.getText());

                SaborRN saborRN = new SaborRN();
                saborRN.atualizarSabor(saborVO);

                popularTabelaSabor();

                JOptionPane.showMessageDialog(null, "Sabor de Pizza Atualizado com Sucesso.", "Edição Sabor de Pizza", JOptionPane.INFORMATION_MESSAGE);

                txtCodigo.setEnabled(false);
                txtCodigo.setText("");
                txtIngredientes.setEditable(false);
                txtIngredientes.setText("");
                txtPreco.setEnabled(false);
                txtPreco.setText("");
                txtSabor.setEnabled(false);
                txtSabor.setText("");
                rbAtivo.setSelected(true);
                rbInativo.setSelected(false);
                rbDoce.setSelected(true);
                rbSalgada.setSelected(false);

                btCancelar.setEnabled(true);

            } catch (SQLException ex) {

                JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage(), "Edição Sabor de Pizza", JOptionPane.ERROR_MESSAGE);

            } catch (Exception ex) {

                JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage(), "Edição Sabor de Pizza", JOptionPane.ERROR_MESSAGE);
            }
        }

    }
