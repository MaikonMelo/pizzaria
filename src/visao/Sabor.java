    @Override
    public void btEditarActionPerformed(ActionEvent e) {
        txtIngredientes.setEnabled(true);
        txtPreco.setEnabled(true);
        txtSabor.setEnabled(true);
        rbDoce.setEnabled(true);
        rbSalgada.setEnabled(true);
        rbAtivo.setEnabled(true);
        rbInativo.setEnabled(true);

        String valorTemporario = ((String) tbTemplate.getValueAt(tbTemplate.getSelectedRow(), 2));

        txtCodigo.setText((String) tbTemplate.getValueAt(tbTemplate.getSelectedRow(), 0));
        txtSabor.setText((String) tbTemplate.getValueAt(tbTemplate.getSelectedRow(), 1));
        txtPreco.setText(valorTemporario.replace(" ", "").replace("R", "").replace("$", "").replace(",", "."));

        if ("Doce".equals(tbTemplate.getValueAt(tbTemplate.getSelectedRow(), 3))) {
            rbDoce.setSelected(true);
            rbSalgada.setSelected(false);
        } else {
            rbSalgada.setSelected(true);
            rbDoce.setSelected(false);
        }

        if ("Ativo".equals((String) tbTemplate.getValueAt(tbTemplate.getSelectedRow(), 4))) {
            rbAtivo.setSelected(true);
            rbInativo.setSelected(false);
        } else {
            rbInativo.setSelected(true);
            rbAtivo.setSelected(false);
        }

        txtIngredientes.setText((String) tbTemplate.getValueAt(tbTemplate.getSelectedRow(), 5));
    }

    @Override
    public void btExcluirActionPerformed(ActionEvent e) {
        txtIngredientes.setEnabled(false);
        txtPreco.setEnabled(false);
        txtSabor.setEnabled(false);
        rbDoce.setEnabled(false);
        rbSalgada.setEnabled(false);

        int n = JOptionPane.showConfirmDialog(null, "Deseja excluir o sabor de pizza? ", "Sabor", JOptionPane.YES_NO_OPTION);
        if (n == 0) {

            try {

                int codigo;
                codigo = Integer.parseInt((String) tbTemplate.getValueAt(tbTemplate.getSelectedRow(), 0));

                SaborRN saborRN = new SaborRN();
                saborRN.excluirSabor(codigo);

                popularTabelaSabor();

                JOptionPane.showMessageDialog(null, "Sabor excluído com Sucesso.", "Exclusão de Sabor", JOptionPane.INFORMATION_MESSAGE);

            } catch (SQLException sqle) {

                JOptionPane.showMessageDialog(null, "Erro: " + sqle.getMessage(), "Exclusão de Sabor", JOptionPane.ERROR_MESSAGE);

            } catch (Exception ex) {

                JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage(), "Exclusão de Sabor", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

